# 4. Escriba un programa para verificar si un número es divisible o no por 5 y 11.
x= int(input("digite un número "))
    
if x%5==0:
    print("el número es divisible por 5")
else:
    print("el número no es divisible por 5")
if x%11==0:
    print("el número es divisible por 11")
else:
    print("el número no es divisible por 11")
